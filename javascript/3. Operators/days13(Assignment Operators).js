var a = 10;
var b = 20;

let c; // the 'c' is defined


// Assignment Operators

c = a + b;
console.log(c);     // = (Simple Assignment)

c += a;
console.log(c);     // += (Add and Assignment) 'c = c + a'

c -= b;
console.log(c);     // -= (Subtract and Assignment) 'c = c - a'

c *= a;
console.log(c);     // *= (Multiply and Assignment) 'c = c * a'

c /= a;
console.log(c);     // /= (Divide and Assignment) 'c = c / a'

c %= b;
console.log(c);     // %= (Modules and Assignment) 'c = c % a'