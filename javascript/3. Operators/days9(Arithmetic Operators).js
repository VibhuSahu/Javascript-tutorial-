let x = 10;
let y = 3;


// Arithmetic Operators
console.log(x + y);     // Addition
console.log(x - y);     // Subtraction
console.log(x * y);     // Multiplication
console.log(x / y);     // Division
console.log(x ** y);    // it means x to the power y
console.log(x % y);     // modulus


// In this the value of x goint to Increment or Decrement after x or y printed on screen
console.log(x--);       // Decrement
console.log(y++);       // Increment


// In this the value of x goint to Increment or Decrement befor x or y printed on screen
console.log(--x);       // Decrement
console.log(++y);       // Increment