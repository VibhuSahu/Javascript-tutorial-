let x = 10;
let y = 20;


// Comparison Operators

console.log(x == y);    // Lose Equality Operator (value only)
console.log(x === y);   // Strict Equality Operator (Type + Value)


console.log(x != y);    // Lose Not Equal Operator (value only)
console.log(x !== y);   // Strict Not Equal Operator (Type + Value) 


console.log(x > y);     // 'x' is Greater than 'y'
console.log(x >= y);    // 'x' is Greater than 'y' or Equal to


console.log(x < y);     // 'x' is Less than 'y'
console.log(x <= y);    // 'x' is Less than 'y' or Equal to