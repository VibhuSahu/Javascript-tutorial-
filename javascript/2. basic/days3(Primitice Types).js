// Primitice Types

// 1. string Literal
let name = 'mosh';
console.log(name);


// 2. Number Literal
let age = 30;
let Age = 3.1;
console.log(age);
console.log(Age);


// 3. Boolean Literal
let isApproved = false;
console.log(isApproved);


// 4. Undefined
let firstName;
let FirstName = undefined;
console.log(firstName);
console.log(FirstName);


// 5. null
let lastName = null;
console.log(lastName);